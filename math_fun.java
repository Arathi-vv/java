public class math_fun{
   public static void main(String[] args){
    System.out.println("The 2 values 10,20");
    System.out.println("Maximum value is:"+Math.max(10,20));
    System.out.println("Minimum value is:"+Math.min(10,20));
    System.out.println("Square root of 9:"+Math.sqrt(9));
    System.out.println("Absolute value of -5:"+Math.abs(-5));
    System.out.println("Random number(0-1):"+Math.random());
    System.out.println("Random number(1000):"+Math.random()*1000);
 }}
